public class RealLiteral {
    public static void main(String[] args) {
        double literal1 = 1.0;
        double literal2 = 1.000000000000000001;
        double literal3 = 1.999999999999999999;

        System.out.println("literal1:" + literal1);
        System.out.println("literal2:" + literal2);
        System.out.println("literal3:" + literal3);
    }
}
