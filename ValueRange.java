public class ValueRange {
    public static void main(String[] args) {
        System.err.println("byte MIN, MAX :" + Byte.MIN_VALUE + "~" + Byte.MAX_VALUE);
        System.err.println("sort MIN, MAX :" + Short.MIN_VALUE + "~" + Short.MAX_VALUE);
        System.err.println("int MIN, MAX :" + Integer.MIN_VALUE + "~" + Integer.MAX_VALUE);
        System.err.println("long MIN, MAX :" + Long.MIN_VALUE + "~" + Long.MAX_VALUE);
        System.err.println("float MIN, MAX :" + Float.MIN_VALUE + "~" + Float.MAX_VALUE);
        System.err.println("double MIN, MAX :" + Double.MIN_VALUE + "~" + Double.MAX_VALUE);
    }
}
